package persistence.Utils;

public class GRADEBean {
    String tab1;
    String tab2;
    String tab3;
    String tab5;

    public GRADEBean(String tab1, String tab2, String tab3,  String tab5) {
        this.tab1 = tab1;
        this.tab2 = tab2;
        this.tab3 = tab3;
        this.tab5 = tab5;
    }

    public String getTab5() {
        return tab5;
    }

    public void setTab5(String tab5) {
        this.tab5 = tab5;
    }

    public String getTab1() {
        return tab1;
    }

    public void setTab1(String tab1) {
        this.tab1 = tab1;
    }

    public String getTab2() {
        return tab2;
    }

    public void setTab2(String tab2) {
        this.tab2 = tab2;
    }

    public String getTab3() {
        return tab3;
    }

    public void setTab3(String tab3) {
        this.tab3 = tab3;
    }

}
